"""
evaluate_test_data.py
=====================
1. Trains ONE LSTM model on all 6 patients' training CSVs combined
2. Evaluates on each patient's official test CSV (never seen during training)
3. Outputs:
   - Per-patient RMSE and MAE in mg/dL
   - Clarke Error Grid plot per patient
   - Prediction time-series plot per patient
   - Final summary table as CSV and PNG

Run from your project root:
    python process_test_data.py   <- first, to generate test CSVs with features
    python evaluate_test_data.py  <- then this

Expected folder structure after process_test_data.py:
    data/processed/bolus_meal_steps_data/csv/
        patient_559-ws-training.csv
        patient_559-ws-testing.csv   <- generated by process_test_data.py
        ... etc
"""

import os
import re
import numpy as np
import pandas as pd
import matplotlib
matplotlib.use("Agg")
import matplotlib.pyplot as plt
from sklearn.metrics import mean_absolute_error, mean_squared_error
from sklearn.preprocessing import StandardScaler
import keras

# ─────────────────────────────────────────────────────────────
# CONFIG
# ─────────────────────────────────────────────────────────────
FEATURES_DIR = "data/processed/bolus_meal_steps_data/csv"  # both training + testing CSVs live here
OUTPUT_DIR   = "test_evaluation_results"

# Best hyperparameters from trial_id 04
UNITS          = 32
DROPOUT_RATE   = 0.1
LEARNING_RATE  = 0.001
NUM_LSTM_LAYERS  = 2
NUM_DENSE_LAYERS = 2

TIME_STEPS = 36   # 3 hours of history  (36 × 5 min)
HORIZON    = 6    # 30-minute prediction ( 6 × 5 min)
EPOCHS     = 50
BATCH_SIZE = 32

os.makedirs(OUTPUT_DIR, exist_ok=True)


# ─────────────────────────────────────────────────────────────
# DATA LOADING
# ─────────────────────────────────────────────────────────────

def extract_patient_id(filename):
    match = re.search(r'patient_(\d+)', filename)
    return match.group(1) if match else None


def load_csv_dir(folder, split="training"):
    """Load CSVs filtered by split (training/testing) from folder."""
    frames = []
    for f in sorted(os.listdir(folder)):
        if not f.endswith('.csv'):
            continue
        if split not in f:
            continue
        pid = extract_patient_id(f)
        df  = pd.read_csv(os.path.join(folder, f))
        df['patient_id'] = pid
        frames.append(df)
        print(f"  Loaded {f}  ({len(df)} rows)")
    return pd.concat(frames, ignore_index=True)


# ─────────────────────────────────────────────────────────────
# SEQUENCE BUILDER
# ─────────────────────────────────────────────────────────────

def create_sequences(data_array, target_array, time_steps=36, horizon=6):
    X, y = [], []
    for i in range(len(data_array) - time_steps - horizon + 1):
        x_window = data_array[i : i + time_steps]
        y_target = target_array[i + time_steps + horizon - 1]
        if np.isnan(x_window).any() or np.isnan(y_target):
            continue
        X.append(x_window)
        y.append(y_target)
    return np.array(X), np.array(y)


# ─────────────────────────────────────────────────────────────
# MODEL
# ─────────────────────────────────────────────────────────────

def build_lstm_model(input_shape):
    model = keras.models.Sequential()

    for i in range(NUM_LSTM_LAYERS):
        return_seq = (i < NUM_LSTM_LAYERS - 1)   # True for all but last LSTM
        if i == 0:
            model.add(keras.layers.LSTM(UNITS, input_shape=input_shape,
                                        return_sequences=return_seq))
        else:
            model.add(keras.layers.LSTM(UNITS, return_sequences=return_seq))

    model.add(keras.layers.Dropout(DROPOUT_RATE))

    for _ in range(NUM_DENSE_LAYERS):
        model.add(keras.layers.Dense(UNITS // 2, activation='relu'))

    model.add(keras.layers.Dense(1))

    model.compile(
        optimizer=keras.optimizers.Adam(learning_rate=LEARNING_RATE),
        loss='mean_squared_error',
        metrics=['mean_absolute_error',
                 keras.metrics.RootMeanSquaredError(name='rmse')]
    )
    model.summary()
    return model


# ─────────────────────────────────────────────────────────────
# CLARKE ERROR GRID
# ─────────────────────────────────────────────────────────────

def classify_clarke_zone(ref, pred):
    if ref < 70:
        if pred < 70:
            return 'A'
        elif pred <= 180:
            return 'D'
        else:
            return 'E'
    elif ref <= 180:
        if abs(pred - ref) / ref <= 0.20:
            return 'A'
        elif pred < 70:
            return 'C'
        elif pred > 180:
            return 'C'
        elif pred < ref * 0.70:
            return 'D'
        else:
            return 'B'
    else:  # ref > 180
        if abs(pred - ref) / ref <= 0.20:
            return 'A'
        elif pred < 70:
            return 'E'
        elif pred <= 180:
            return 'D'
        else:
            return 'B'


def plot_clarke_error_grid(y_true, y_pred, patient_id):
    ref  = y_true.flatten()
    pred = y_pred.flatten()

    zones = np.array([classify_clarke_zone(r, p) for r, p in zip(ref, pred)])
    zone_labels = ['A', 'B', 'C', 'D', 'E']
    total  = len(zones)
    counts = {z: int(np.sum(zones == z)) for z in zone_labels}
    pcts   = {z: 100 * counts[z] / total   for z in zone_labels}

    colors = {'A': '#2ecc71', 'B': '#3498db',
              'C': '#f39c12', 'D': '#9b59b6', 'E': '#e74c3c'}

    fig, ax = plt.subplots(figsize=(7, 7))
    for z in zone_labels:
        mask = zones == z
        ax.scatter(ref[mask], pred[mask], c=colors[z], s=8,
                   alpha=0.5, label=f"Zone {z} ({pcts[z]:.1f}%)")

    # ±20 % corridor
    x = np.linspace(0, 400, 400)
    ax.plot(x, x * 1.20, 'k--', linewidth=0.8)
    ax.plot(x, x * 0.80, 'k--', linewidth=0.8)
    ax.plot(x, x,        'k-',  linewidth=0.6, alpha=0.4)

    # Clinical boundary lines
    for v in [70, 180]:
        ax.axvline(v, color='grey', linewidth=0.5, linestyle=':')
        ax.axhline(v, color='grey', linewidth=0.5, linestyle=':')

    ax.set_xlim(0, 400);  ax.set_ylim(0, 400)
    ax.set_xlabel("Reference Glucose (mg/dL)", fontsize=12)
    ax.set_ylabel("Predicted Glucose (mg/dL)", fontsize=12)
    ax.set_title(
        f"Clarke Error Grid – Patient {patient_id}\n"
        f"A={pcts['A']:.1f}%  B={pcts['B']:.1f}%  "
        f"C={pcts['C']:.1f}%  D={pcts['D']:.1f}%  E={pcts['E']:.1f}%",
        fontsize=11
    )
    ax.legend(loc='upper left', markerscale=2, fontsize=9)
    ax.grid(True, alpha=0.2)

    path = os.path.join(OUTPUT_DIR, f"patient_{patient_id}_clarke.png")
    fig.savefig(path, dpi=120, bbox_inches='tight')
    plt.close(fig)
    print(f"  Clarke plot saved → {path}")
    return counts, pcts


# ─────────────────────────────────────────────────────────────
# PREDICTION PLOT
# ─────────────────────────────────────────────────────────────

def plot_predictions(y_true, y_pred, patient_id):
    fig, axes = plt.subplots(1, 2, figsize=(14, 4))

    # Time series
    n = min(300, len(y_true))
    axes[0].plot(y_true[:n],  label='True',      linewidth=1.2)
    axes[0].plot(y_pred[:n],  label='Predicted', linewidth=1.2, linestyle='--')
    axes[0].set_title(f"Patient {patient_id} – Time Series (first {n} steps)")
    axes[0].set_xlabel("Time steps (× 5 min)")
    axes[0].set_ylabel("Glucose (mg/dL)")
    axes[0].legend(); axes[0].grid(True, alpha=0.3)

    # Scatter
    mn = min(y_true.min(), y_pred.min()) - 5
    mx = max(y_true.max(), y_pred.max()) + 5
    axes[1].scatter(y_true, y_pred, alpha=0.25, s=6)
    axes[1].plot([mn, mx], [mn, mx], 'r--', linewidth=1)
    axes[1].set_title(f"Patient {patient_id} – Scatter")
    axes[1].set_xlabel("True Glucose (mg/dL)")
    axes[1].set_ylabel("Predicted Glucose (mg/dL)")
    axes[1].grid(True, alpha=0.3)

    plt.tight_layout()
    path = os.path.join(OUTPUT_DIR, f"patient_{patient_id}_predictions.png")
    fig.savefig(path, dpi=120, bbox_inches='tight')
    plt.close(fig)
    print(f"  Prediction plot saved → {path}")


