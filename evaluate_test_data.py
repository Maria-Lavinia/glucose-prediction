def save_summary_table(summary_df):
    # Save CSV
    csv_path = os.path.join(OUTPUT_DIR, "test_evaluation_summary.csv")
    summary_df.to_csv(csv_path, index=False)
    print(f"\nSummary CSV saved → {csv_path}")

    # Save as PNG table
    display_cols = ['patient_id', 'RMSE_mgdL', 'MAE_mgdL',
                    'Zone_A_%', 'Zone_B_%', 'Zone_C_%', 'Zone_D_%', 'Zone_E_%']
    plot_df = summary_df[display_cols].copy()

    fig, ax = plt.subplots(figsize=(13, len(plot_df) * 0.55 + 1.2))
    ax.axis('off')

    col_labels = ['Patient', 'RMSE\n(mg/dL)', 'MAE\n(mg/dL)',
                  'Zone A\n(%)', 'Zone B\n(%)', 'Zone C\n(%)',
                  'Zone D\n(%)', 'Zone E\n(%)']

    cell_text = []
    for _, row in plot_df.iterrows():
        cell_text.append([
            str(row['patient_id']),
            f"{row['RMSE_mgdL']:.2f}",
            f"{row['MAE_mgdL']:.2f}",
            f"{row['Zone_A_%']:.1f}",
            f"{row['Zone_B_%']:.1f}",
            f"{row['Zone_C_%']:.1f}",
            f"{row['Zone_D_%']:.1f}",
            f"{row['Zone_E_%']:.1f}",
        ])

    # Colour the MEAN row differently
    n_data = len(plot_df) - 1
    row_colors = [['#f0f0f0'] * len(col_labels)] * n_data + \
                 [['#d0e8ff']  * len(col_labels)]

    tbl = ax.table(
        cellText=cell_text,
        colLabels=col_labels,
        cellLoc='center',
        loc='center',
        cellColours=row_colors
    )
    tbl.auto_set_font_size(False)
    tbl.set_fontsize(10)
    tbl.scale(1, 1.6)

    # Bold header
    for j in range(len(col_labels)):
        tbl[0, j].set_facecolor('#2c3e50')
        tbl[0, j].set_text_props(color='white', fontweight='bold')

    ax.set_title(
        "LSTM 30-min Glucose Prediction – Official Test Set Evaluation",
        fontsize=13, fontweight='bold', pad=16
    )

    table_path = os.path.join(OUTPUT_DIR, "test_evaluation_table.png")
    fig.savefig(table_path, dpi=150, bbox_inches='tight')
    plt.close(fig)
    print(f"Summary table image saved → {table_path}")


# ─────────────────────────────────────────────────────────────
# MAIN
# ─────────────────────────────────────────────────────────────

def main():
    print("=" * 60)
    print("STEP 1 – Loading training data")
    print("=" * 60)
    train_df = load_csv_dir(FEATURES_DIR, split="training")

    features = [col for col in train_df.columns
                if col not in ['patient_id', 'timestamp', 'glucose']]
    target   = 'glucose'
    print(f"\nFeatures ({len(features)}): {features}")

    # ── Fit scalers on ALL training data ──────────────────────
    print("\nFitting scalers on all training data...")
    scaler_X = StandardScaler()
    scaler_y = StandardScaler()
    scaler_X.fit(train_df[features])
    scaler_y.fit(train_df[[target]])

    # ── Build training sequences for every patient ─────────────
    print("\nBuilding training sequences...")
    X_list, y_list = [], []
    for pid in sorted(train_df['patient_id'].unique()):
        p = train_df[train_df['patient_id'] == pid].copy()
        Xs = scaler_X.transform(p[features])
        ys = scaler_y.transform(p[[target]])
        combined = np.hstack([Xs, ys])
        xp, yp = create_sequences(combined, ys, TIME_STEPS, HORIZON)
        X_list.append(xp);  y_list.append(yp)
        print(f"  Patient {pid}: {xp.shape[0]} sequences")

    X_train = np.concatenate(X_list)
    y_train = np.concatenate(y_list)
    print(f"\nTotal training sequences: {X_train.shape}")

    # ── Train model ────────────────────────────────────────────
    print("\n" + "=" * 60)
    print("STEP 2 – Training LSTM model")
    print("=" * 60)
    n_features = X_train.shape[2]
    model = build_lstm_model(input_shape=(TIME_STEPS, n_features))

    early_stop = keras.callbacks.EarlyStopping(
        monitor='val_loss', patience=10, restore_best_weights=True, verbose=1
    )
    model.fit(
        X_train, y_train,
        epochs=EPOCHS,
        batch_size=BATCH_SIZE,
        validation_split=0.1,
        callbacks=[early_stop],
        verbose=1
    )

    # Save model for future use
    model_path = os.path.join(OUTPUT_DIR, "lstm_final_model.h5")
    model.save(model_path)
    print(f"\nModel saved → {model_path}")

    # ── Evaluate on test CSVs ──────────────────────────────────
    print("\n" + "=" * 60)
    print("STEP 3 – Evaluating on official test data")
    print("=" * 60)
    test_df = load_csv_dir(FEATURES_DIR, split="testing")

    summary_rows = []

    for pid in sorted(test_df['patient_id'].unique()):
        print(f"\n── Patient {pid} ──")
        pat = test_df[test_df['patient_id'] == pid].copy()

        # Scale using the TRAINING scaler (never refit on test data)
        Xs = scaler_X.transform(pat[features])
        ys = scaler_y.transform(pat[[target]])
        combined = np.hstack([Xs, ys])

        X_test, y_test = create_sequences(combined, ys, TIME_STEPS, HORIZON)
        if len(X_test) == 0:
            print(f"  ⚠  Not enough data – skipping.")
            continue
        print(f"  Test sequences: {X_test.shape}")

        # Predict & inverse-transform to mg/dL
        y_pred_scaled = model.predict(X_test, verbose=0)
        y_pred = scaler_y.inverse_transform(y_pred_scaled).flatten()
        y_true = scaler_y.inverse_transform(y_test).flatten()

        # Metrics
        rmse = np.sqrt(mean_squared_error(y_true, y_pred))
        mae  = mean_absolute_error(y_true, y_pred)
        print(f"  RMSE : {rmse:.2f} mg/dL")
        print(f"  MAE  : {mae:.2f} mg/dL")

        # Clarke Error Grid
        counts, pcts = plot_clarke_error_grid(y_true, y_pred, pid)
        print(f"  Clarke → A:{pcts['A']:.1f}%  B:{pcts['B']:.1f}%  "
              f"C:{pcts['C']:.1f}%  D:{pcts['D']:.1f}%  E:{pcts['E']:.1f}%")

        # Prediction plots
        plot_predictions(y_true, y_pred, pid)

        summary_rows.append({
            'patient_id' : pid,
            'n_sequences': len(y_true),
            'RMSE_mgdL'  : round(rmse, 2),
            'MAE_mgdL'   : round(mae,  2),
            'Zone_A_%'   : round(pcts['A'], 1),
            'Zone_B_%'   : round(pcts['B'], 1),
            'Zone_C_%'   : round(pcts['C'], 1),
            'Zone_D_%'   : round(pcts['D'], 1),
            'Zone_E_%'   : round(pcts['E'], 1),
        })

    # ── Summary table ──────────────────────────────────────────
    print("\n" + "=" * 60)
    print("FINAL RESULTS")
    print("=" * 60)
    summary_df = pd.DataFrame(summary_rows)

    mean_row          = summary_df.select_dtypes(include='number').mean().round(2).to_dict()
    std_row           = summary_df.select_dtypes(include='number').std().round(2).to_dict()
    mean_row['patient_id'] = 'MEAN'
    std_row['patient_id']  = 'STD'

    summary_df = pd.concat(
        [summary_df,
         pd.DataFrame([mean_row]),
         pd.DataFrame([std_row])],
        ignore_index=True
    )

    print(summary_df.to_string(index=False))
    save_summary_table(summary_df)

    print("\n✓ All results saved to:", OUTPUT_DIR)
    return summary_df


if __name__ == "__main__":
    main()